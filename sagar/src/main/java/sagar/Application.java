package sagar;
public class Application{
	public static void main (String[] args) {
		System.out.println("arithmetic operations");
		
	}
	public static Object add(double d,double e) {
		return d+e;
		
	}
	public static Object sub(double d,double e) {
		return d-e;
		
	}
	public static Object mul(double d,double e) {
		return d*e;
		
	}
	public static Object div(double d,double e) {
		return d/e;
		
	}
}
